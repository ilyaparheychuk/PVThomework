public class Calculator {


    public int FirstMethodSum(int a, int b) {
        return a+b;
    }

    public int SecondMethodMult(int a, int b){
        return a*b;
    }

    public int ThirdMetodSqrt(int a) {
        return a*a;
    }

    public int ForthMethodMin(int a, int b){
        return Math.min(a, b);
    }

    public int FifthMethodMax(int a, int b) {

        return Math.max(a, b);
    }

}
