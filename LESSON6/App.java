package PVThome;

public class App {

    public double sumFirstMethod(int a, int b) {
        return a + b;
    }

    public int multSecondMethod(int a, int b) {
        return a * b;
    }

    public double sqrtThirdMethod(int a) {
        return Math.sqrt(a);
    }

    public int minForthMethod(int a, int b) {
        return Math.min(a, b);
    }

    public int maxFifthMethod(int a, int b) {
        return Math.max(a, b);
    }

    public int divisionSixMethod(int a, int b) {
        return a / b;
    }

    public double powSevenMethod(int a) {
        return Math.pow(a, 3);

    }

    public int lengthEightMethod(String str) {
        return str.length();
    }


}
